	<p>Chiropractic is a branch of the healing arts which is based upon the understanding that good health depends, in part, upon a normally functioning nervous system, especially the spine, and the nerves extending from the spine to all parts of the body. </p>
	<p>Chiropractic stresses the idea that the cause of many disease processes begins with the body's inability to adapt to its environment. It looks to address these diseases not by the use of drugs and chemicals, but by locating and adjusting a musculoskeletal area of the body which is functioning improperly.</p>
	<h2>Acupuncture - What is it?</h2>
	<p>Acupuncture literally means "needle piercing," the practice of inserting very fine needles into the skin to stimulate specific anatomic points in the body (called acupoints or acupuncture points) for therapeutic purposes. Along with the usual method of puncturing the skin with the fine needles, the practitioners of acupuncture also use heat, pressure, friction, suction, or impulses of electromagnetic energy to stimulate the points. The acupoints (acupuncture points) are stimulated to balance the movement of energy (qi) in the body to restore health. </p>
